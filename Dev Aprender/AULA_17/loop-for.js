// LOOP FOR
for (let i = 1; i <= 5; i++) {
    console.log('Estou aprendendo!', i)
}
console.log('------------------------------')
for (let i = 1; i <= 5; i++) {
    if(i % 2 !== 0){
        console.log('Estou aprendendo!', i)
    }
}
console.log('------------------------------')
for (let i = 5; i >= 1; i--) {
    if(i % 2 !== 0){
        console.log('Estou aprendendo!', i)
    }
}