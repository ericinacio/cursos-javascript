// ENCONTRANDO ELEMENTOS NA ARRAY(REFERÊNCIAS)
const numeros = [1,2,3,1,4]
console.log(numeros.indexOf(2)) // RETORNAR ÍNDICE

console.log(numeros.lastIndexOf(1)) // ULTIMA OCORRÊNCIA

console.log(numeros.indexOf(2) !== -1) // EXISTE ESSE NUMERO NA ARRAY

console.log(numeros.includes(2)) // EXISTE ESSE NUMERO NA ARRAY - NOVA FORMA