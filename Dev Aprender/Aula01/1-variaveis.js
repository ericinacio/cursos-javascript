// camelCase - Maneira certa de nomear variáveis (Ex: nomeCompleto)

let nao = "Não";
let a = "á"
let teste = "Teste"
console.log(nao);
console.log(a);
console.log(teste);

// [ let ] - Pode ser alterado ao longo do código.
let valorIngressoAdulto = 20;
valorIngressoAdulto = 30;
console.log(valorIngressoAdulto);

// [ const ] - Não pode ser alterado ao longo do código.
const valorIngresso = 20;
console.log(valorIngresso);

